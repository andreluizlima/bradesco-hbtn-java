public class Mensagem {
    private String texto;
    private TipoMensagem tipo;

    
    public String getTexto() {
        return texto;
    }
    public void setTexto(String texto) {
        this.texto = texto;
    }
    public TipoMensagem getTipo() {
        return tipo;
    }
    public void setTipo(TipoMensagem tipo) {
        this.tipo = tipo;
    }

    
}
